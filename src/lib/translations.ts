export type Language = 'ar' | 'en';

export const getTranslation = (key: keyof typeof translations, lang: Language = 'ar'): string => {
  const translation = translations[key];
  if (!translation) {
    console.warn(`Translation missing for key: ${key}`);
    return key;
  }
  return translation[lang] || translation['ar'] || key;
};

export const formatTranslation = (key: keyof typeof translations, lang: Language, params: Record<string, string> = {}): string => {
  let text = getTranslation(key, lang);
  Object.entries(params).forEach(([param, value]) => {
    text = text.replace(`{${param}}`, value);
  });
  return text;
};
